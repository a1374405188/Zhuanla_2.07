package com.beikbank.android.data;

public class IncomeInfo {
	
	public double interestM;
	public double interestT;
	public double interestY;
	public double interestW;
	public double getInterestM() {
		return interestM;
	}
	public void setInterestM(double interestM) {
		this.interestM = interestM;
	}
	public double getInterestT() {
		return interestT;
	}
	public void setInterestT(double interestT) {
		this.interestT = interestT;
	}
	public double getInterestY() {
		return interestY;
	}
	public void setInterestY(double interestY) {
		this.interestY = interestY;
	}
	public double getInterestW() {
		return interestW;
	}
	public void setInterestW(double interestW) {
		this.interestW = interestW;
	}
	
}
